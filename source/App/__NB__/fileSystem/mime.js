//驼峰转连字符
function convertToHyphen( input){
    
    var result= input.replace(/([A-Z])/g,"-$1").toLowerCase();
    return result;
}

var mime_object={},ext_object={};

function ergodic(input,parent){

    

    for(var x in input){
        var item=input[x];
        var key=convertToHyphen(x.replace('_','+'));

        var next_parent=parent;

        if(!next_parent){
            next_parent=key+'/';
            ergodic(item,next_parent);
        }
        else {

            if(typeof item =='string'){

                if(item==='')item=key;

                next_parent+=key;
                var ext=item.split(',');

                mime_object[next_parent]=ext[0];

                for(var i=0;i<ext.length;i++){
                    ext_object[ext[i]]=next_parent;
                }
            }
            else{
                next_parent+=key+'.';
                ergodic(item,next_parent);
            }

           
        }

    }
}

